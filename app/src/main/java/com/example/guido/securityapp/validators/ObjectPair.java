public class ObjectPair {
    private Object obj1;
    private Object obj2;

    public ObjectPair(Object obj1, Object obj2) {
        this.obj1 = obj1;
        this.obj2 = obj2;
    }

    public Object getObj1() {
        return obj1;
    }

    public Object getObj2() {
        return obj2;
    }
}
